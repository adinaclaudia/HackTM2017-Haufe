var introduction = [
    "What do you want to know about us ?"
];
var teamName = [
    "Our team is called Alexa and friends"
];
var projectName = [
    "Our project is called play with Alexa"
];
var projectDescription = [
    "We worked on a project which enables people to play text based retro games using Amazon Alexa only by using the voice"    
];

